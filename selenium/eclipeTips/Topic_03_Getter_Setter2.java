package eclipeTips;

public class Topic_03_Getter_Setter2 {

	private String carName;
	private String carType;
	private String carColor;
	private String carMachine;
	
	// right click - Source - Generate Getter/Setter
	
	String getCarName() {
		return carName;
	}
	void setCarName(String carName) {
		this.carName = carName;
	}
	String getCarType() {
		return carType;
	}
	void setCarType(String carType) {
		this.carType = carType;
	}
	String getCarColor() {
		return carColor;
	}
	void setCarColor(String carColor) {
		this.carColor = carColor;
	}
	String getCarMachine() {
		return carMachine;
	}
	void setCarMachine(String carMachine) {
		this.carMachine = carMachine;
	}




	
	
}

	
